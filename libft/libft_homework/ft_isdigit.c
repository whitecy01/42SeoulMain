#include "libft.h"

int	ft_isdigit(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'A'))
		return (0);
	else if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
